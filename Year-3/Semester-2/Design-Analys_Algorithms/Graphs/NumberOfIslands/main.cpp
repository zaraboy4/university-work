#include <vector>
#include <iostream>
#include <queue>

void bfs(const std::vector<std::vector<int>>& g, int start, std::vector<bool>& visited) {
    std::queue<int> q;
    q.push(start);
    visited[start] = true;
    while (!q.empty()) {
        int curr = q.front();
        q.pop();
        for (int u : g[curr]) {
            if (visited[u] == false) {
                visited[u] = true;
                q.push(u);
            }   
        }
    }
} 

int numIslands(std::vector<std::vector<char>>& grid) {
    int m = grid.size();
    int n = grid[0].size();
    std::vector<std::vector<int>> A(m);
    int cnt = -1;
    for (int i = 0; i < m; ++i) {
        A[i].resize(n);
        for (int j = 0; j < n; ++j) {
            if (grid[i][j] - '0' == 1) {
                A[i][j] = ++cnt;
            }
            else {
                A[i][j] = -1;
            }
        }
    }

    std::vector<std::vector<int>> g(cnt + 1);
    for (int i = 0; i < m; ++i) {
        for (int j = 0; j < n; ++j) {
            int curr = A[i][j];
            if (curr != -1) {
                if (j >= 1 && A[i][j-1] != -1) {
                    g[curr].push_back(A[i][j-1]);
                }
                if (i >= 1 && A[i-1][j] != -1) {
                    g[curr].push_back(A[i-1][j]);
                }
                if (j < n - 1 && A[i][j+1] != -1) {
                    g[curr].push_back(A[i][j+1]);
                }
                if (i < m - 1 && A[i+1][j] != -1) {
                    g[curr].push_back(A[i+1][j]);
                }
            }
        }
    }

    int countComponents = 0;
    std::vector<bool> visited(g.size(), false);
    for (int i = 0; i < g.size(); ++i) {
        if (visited[i] == false) {
            bfs(g, i, visited);
            // for (int v : visited) {
            //     std::cout << v << ' ';
            // }
            // std::cout << '\n';
            ++countComponents;
        }
    }

    return countComponents;
}

