#include <vector>
#include <iostream>

void dfsRec(std::vector<std::vector<char>>& grid, int i, int j) {
    if (i < 0 || j < 0 
            || i > grid.size() - 1 || j > grid[0].size() - 1) // check for invalid indexes
        return;
    if (grid[i][j] == '0') // check if cell is visited
        return;
    grid[i][j] = '0'; // 1 -> 0 in order this cell to be not visited again
    dfsRec(grid, i, j - 1);
    dfsRec(grid, i - 1, j);
    dfsRec(grid, i, j + 1);
    dfsRec(grid, i + 1, j);
}

int numIslands(std::vector<std::vector<char>>& grid) {
    int m = grid.size();
    int n = grid[0].size();
    int cnt = 0;
    for (int i = 0; i < m; ++i) {
        for (int j = 0; j < n; ++j) {
            if (grid[i][j] == '1') {
                dfsRec(grid, i, j);
                ++cnt;
            }
        }
    }
    return cnt;
}

